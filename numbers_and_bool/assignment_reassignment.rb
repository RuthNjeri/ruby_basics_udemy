a = 10

p a # => 10

a = a + 5 # => 15
p a # => 15

#  a = a + 5 is equivalent to
p a += 5 # => 20

p b = 100 # => 100

p b - 50 # => 50

p b -= 50 # Equivalent to b = b -50 => 50

p c = 3 # 3

p c * 4 # => 12
p c *= 4 # => 12 (c = c * 4)

